#Por lo general el patron Builder se divide en 3 partes:
#Base class: Donde se define el objeto o base objeto
#Builder: Donde se definen las clases de cada tipo de objeto
#Director: Es el encargado de dirigir la creacion de objetos regresando los objetos para que puedan ser utilizados como si fuera una interfaz
